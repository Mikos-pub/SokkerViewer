package pl.pronux.sokker.bean;

public class ArenaPlaceCapacity {
	private int standing;
	private int benches;
	private int seats;
	private int terraces;
	private int benchesWithRoof;
	private int seatsWithRoof;
	private int terracesWithRoof;

	public int getStanding() {
		return standing;
	}

	public void setStanding(int standing) {
		this.standing = standing;
	}

	public int getBenches() {
		return benches;
	}

	public void setBenches(int benches) {
		this.benches = benches;
	}

	public int getSeats() {
		return seats;
	}

	public void setSeats(int seats) {
		this.seats = seats;
	}

	public int getTerraces() {
		return terraces;
	}

	public void setTerraces(int terraces) {
		this.terraces = terraces;
	}

	public int getBenchesWithRoof() {
		return benchesWithRoof;
	}

	public void setBenchesWithRoof(int benchesWithRoof) {
		this.benchesWithRoof = benchesWithRoof;
	}

	public int getSeatsWithRoof() {
		return seatsWithRoof;
	}

	public void setSeatsWithRoof(int seatsWithRoof) {
		this.seatsWithRoof = seatsWithRoof;
	}

	public int getTerracesWithRoof() {
		return terracesWithRoof;
	}

	public void setTerracesWithRoof(int terracesWithRoof) {
		this.terracesWithRoof = terracesWithRoof;
	}
}
